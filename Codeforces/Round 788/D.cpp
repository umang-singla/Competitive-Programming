    int st = 1,ed = 1e9;
    while(st<ed){
        int mid = (st+ed)/2;

        int a = mid/3 + (mid%3>0);
        int b = mid/3 + (mid%3>1);
        int c = mid/3;

        int tri = 2*(a*b+b*c+a*c);
        if(tri >= n){
            ed = mid;
        }else{
            st = mid + 1;
        }
    }

